def convert_to_upper(sentence):
    
    """Convert the given sentence to uppercase.
    """
    input_sentence = str(sentence)
    return sentence.upper(input_sentence)
def convert_to_title(sentence):
    """Convert the given sentence to title case.
    """
    input_sentence = str(sentence)
    """Takes a sentence as input and returns the sentence in title case.
    """
    return sentence.title(input_sentence)